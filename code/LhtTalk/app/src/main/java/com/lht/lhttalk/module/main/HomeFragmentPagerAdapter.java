package com.lht.lhttalk.module.main;

import android.support.v4.app.Fragment;
import android.support.v4.app.FragmentManager;
import android.support.v4.app.FragmentPagerAdapter;

import com.lht.lhttalk.base.fragment.BaseFragment;

import java.util.List;

/**
 * Created by chhyu on 2017/7/12.
 */

/* packaged public*/ class HomeFragmentPagerAdapter  extends FragmentPagerAdapter {

    private final List<BaseFragment> fragments;

    public HomeFragmentPagerAdapter(FragmentManager fm, List<BaseFragment> fragments) {
        super(fm);
        this.fragments = fragments;
    }

    @Override
    public Fragment getItem(int position) {
        return fragments.get(position);
    }

    @Override
    public int getCount() {
        return fragments.size();
    }
}
